package entity;

public class SupplyCenter {
    public String supply_center_name;
    public String salesman_id;
    public String director_name;

    public SupplyCenter(String supply_center_name, String salesman_id, String director_name) {
        this.supply_center_name = supply_center_name;
        this.salesman_id = salesman_id;
        this.director_name = director_name;
    }
}
